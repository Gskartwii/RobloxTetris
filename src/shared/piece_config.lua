local function generate_kick_data(base)
    local target = {};
    for i = 1, #base do
        target[i] = {};
        for j = 1, #base do
            target[i][j] = {};
            for k = 1, #base[j] do
                target[i][j][k] = {base[i][k][1] - base[j][k][1], base[i][k][2] - base[j][k][2]};
            end
        end
    end
end

